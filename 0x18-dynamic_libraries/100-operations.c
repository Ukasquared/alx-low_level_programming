/**
 * add - adds two integers
 * @a: integer
 * @b: integer
 *
 * Return: sum of the two integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts b from a
 * @a: integer
 * @b: integer
 *
 * Return: difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two intergers
 * @a: integer
 * @b: integer
 *
 * Return: product of the two intergers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides one integer by another
 * @a: integer
 * @b: integer
 *
 * Return: quotient of the two intergers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds modulus of a number
 * @a: integer
 * @b: integer
 *
 * Return: modulus of a by b
 */
int mod(int a, int b)
{
	return (a % b);
}
